//Switch Statement

let difficulty = 5;

switch(difficulty){
    case 1 :
        console.log("Easy");
        break;
    case 2 : 
        console.log("Normal");
        break;
    case 3 :
        console.log("Hard");
        break;
    default :
        console.log("Invalid Difficulty");
        break;
}


let num = 2;

